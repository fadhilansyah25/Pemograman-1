public class Matematika {
    void pertambahan(int a_FadilArdiansyah, int b_FadilArdiansyah){
        System.out.println("Pertambahan : "+a_FadilArdiansyah+" + "
        +b_FadilArdiansyah+" = "+(a_FadilArdiansyah+b_FadilArdiansyah));
    }

    void pengurangan(int a_FadilArdiansyah, int b_FadilArdiansyah){
        System.out.println("Pengurangan : "+a_FadilArdiansyah+" - "
        +b_FadilArdiansyah+" = "+(a_FadilArdiansyah-b_FadilArdiansyah));
    }

    void perkalian(int a_FadilArdiansyah, int b_FadilArdiansyah){
        System.out.println("Perkalian : "+a_FadilArdiansyah+" x "
        +b_FadilArdiansyah+" = "+(a_FadilArdiansyah*b_FadilArdiansyah));
    }

    void pembagian(int a_FadilArdiansyah, int b_FadilArdiansyah){
        System.out.println("Pembagian : "+a_FadilArdiansyah+" : "
        +b_FadilArdiansyah+" = "+(a_FadilArdiansyah/b_FadilArdiansyah));
    }
}
